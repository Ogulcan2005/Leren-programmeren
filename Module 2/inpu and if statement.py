#elif-statement
a = int(input('getal a'))
b = int(input('getal b'))

if (a > b):
    print('b is het kleinste getal') 
elif(a < b):
    print('a is het kleinste getal')
#####################################################
# else-statement
if (a > b):
    print('b is het kleinste getal')
else:
    print('a en b zijn even groot')
